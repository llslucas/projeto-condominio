  @Override
  public void setVisible(boolean aFlag) {
    super.setVisible(aFlag);
    
    if (aFlag) {
      if (idCondominio != null) {
	Condominio condominio;

	try {
	  condominio = controller.getById(idCondominio);
	} catch (Exception ex) {
	  JOptionPane.showMessageDialog(this, "Falha na conexão com o banco de dados.", "Erro", JOptionPane.ERROR_MESSAGE);
	  return;
	}

	tFieldCnpj.setText(condominio.getCnpj());
	tFieldRazaoSocial.setText(condominio.getRazaoSocial());	
      } else {
	JOptionPane.showMessageDialog(this, "Nenhum condominio foi selecionado.", "Erro", JOptionPane.ERROR_MESSAGE);
      }
    } else {
      idCondominio = null;
      tFieldCnpj.setText("");
      tFieldRazaoSocial.setText("");
    }    
  }

  private void close() {
    JPanel parent = (JPanel) this.getParent();
    CardLayout layout = (CardLayout) parent.getLayout();
    layout.show(parent, "condominios");
  }

