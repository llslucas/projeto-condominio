  private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    String cnpj = tFieldCnpj.getText();
    String razaoSocial = tFieldRazaoSocial.getText();

    Condominio condominio = new Condominio(idCondominio, cnpj, razaoSocial);

    try {
      this.controller.update(condominio);
      JOptionPane.showMessageDialog(this, "Condomínio editado com sucesso!");
      tFieldCnpj.setText("");
      tFieldRazaoSocial.setText("");
      close();
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Falha ao editar condomínio, verifique o erro abaixo: \n" + ex.getMessage());
    }

  }//GEN-LAST:event_btnSalvarActionPerformed

