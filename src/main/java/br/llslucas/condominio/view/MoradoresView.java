  private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnExcluirActionPerformed
    int row = table.getSelectedRow();

    if (row == -1) {
      JOptionPane.showMessageDialog(this, "Selecione uma linha primeiro!", "Erro", JOptionPane.ERROR_MESSAGE);
      return;
    }

    Long id = Long.valueOf((String) table.getValueAt(row, 0));
    Morador morador;

    try {
      morador = this.moradorController.getById(id);
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "O morador selecionado não existe.", "Erro", JOptionPane.ERROR_MESSAGE);
      return;
    }

    int opt = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o morador?", "Aviso",
	    JOptionPane.OK_CANCEL_OPTION);

    try {
      if (opt == 0) {
	this.moradorController.delete(morador);
	preencherTabela();
      }
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this,
	      "Falha ao excluir o morador, verifique o erro abaixo: \n" + ex.getMessage(), "Erro",
	      JOptionPane.ERROR_MESSAGE);
    }
  }// GEN-LAST:event_btnExcluirActionPerformed

