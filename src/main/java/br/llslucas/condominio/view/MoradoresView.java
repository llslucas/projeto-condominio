  public void updateRow() {
    int row = table.getSelectedRow();

    if (row == -1) {
      return;
    }

    Date dataNascimento;

    try {
      dataNascimento = Date.valueOf(LocalDate.parse((String) table.getValueAt(row, 2), DateTimeFormatter.ofPattern("dd/MM/yyyy")).toString());
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Data em formato incorreto (dd/MM/yyyy)", "Erro", JOptionPane.ERROR_MESSAGE);
      preencherTabela();
      return;
    }

    Long id = Long.valueOf((String) table.getValueAt(row, 0));
    String nome = (String) table.getValueAt(row, 1);
    String rg = (String) table.getValueAt(row, 3);
    String cpf = (String) table.getValueAt(row, 4);
    Residencia residencia = (Residencia) table.getValueAt(row, 5);

    Morador morador = new Morador(id, nome, dataNascimento, rg, cpf, residencia.getId());

    try {
      this.moradorController.update(morador);
      JOptionPane.showMessageDialog(this, "Morador atualizado com sucesso!");
      preencherTabela();
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this,
	      "Falha ao alterar o morador, verifique o erro abaixo: \n" + ex.getMessage(), "Erro",
	      JOptionPane.ERROR_MESSAGE);
    }
  }

  public final void preencherTabela() {
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    table.setModel(model);
    table.selectAll();
    model.setRowCount(0);

    Map<Long, Morador> moradores;

    try {
      if (residenciaIdFilter == null) {
	moradores = moradorController.list();
      } else {
	moradores = moradorController.listByResidencia(residenciaIdFilter);
      }

      moradores.forEach((i, morador) -> {
	Residencia residencia;

	try {
	  if (morador.getResidenciaId() != null) {
	    residencia = this.residenciaController.getById(morador.getResidenciaId());
	  } else {
	    residencia = null;
	  }
	} catch (Exception ex) {
	  residencia = null;
	}

	Object[] data = {
	  String.valueOf(i),
	  morador.getNome(),
	  morador.getDataNascimento().toLocalDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")),
	  morador.getRg(),
	  morador.getCpf(),
	  residencia,};

	model.addRow(data);
      });
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this,
	      "Não foi possível realizar a conexão com o banco de dados, verifique se está ativo ou se foi configurado corretamente.",
	      "Erro", JOptionPane.ERROR_MESSAGE);
    }
  }

