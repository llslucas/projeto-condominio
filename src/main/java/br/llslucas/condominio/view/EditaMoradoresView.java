  @Override
  public void setVisible(boolean aFlag) {
    super.setVisible(aFlag);
    if (aFlag) {
      if (idMorador != null) {
	try {
	  Morador morador = moradorController.getById(idMorador);

	  fNome.setText(morador.getNome());
	  fNascimento.setText(morador.getDataNascimento().toLocalDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
	  fRG.setText(morador.getRg());
	  fCPF.setText(morador.getCpf());

	  Residencia residencia;
	  if (morador.getResidenciaId() != null) {
	    residencia = residenciaController.getById(morador.getResidenciaId());
	  } else {
	    residencia = null;
	  }

	  fResidencia.removeAllItems();

	  this.residenciaController.list().forEach((i, res) -> {
	    fResidencia.addItem(res);
	  });
	} catch (Exception ex) {
	  JOptionPane.showMessageDialog(this, "Falha na conexão com o banco de dados", "Erro", JOptionPane.ERROR_MESSAGE);
	}
      } else {
	JOptionPane.showMessageDialog(this, "Nenhum morador foi selecionado.", "Erro", JOptionPane.ERROR_MESSAGE);
      }
    } else {
      idMorador = null;
      fNome.setText("");
      fNascimento.setText("");
      fRG.setText("");
      fCPF.setText("");
      fResidencia.setSelectedIndex(-1);
    }
  }

  private void close() {
    JPanel parent = (JPanel) this.getParent();
    CardLayout layout = (CardLayout) parent.getLayout();
    layout.show(parent, "moradores");
  }

