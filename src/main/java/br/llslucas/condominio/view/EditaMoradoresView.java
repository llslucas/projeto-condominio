  private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    String nome = fNome.getText();
    Date dataNascimento = Date.valueOf(LocalDate.parse(fNascimento.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy")).toString());
    String rg = fRG.getText();
    String cpf = fCPF.getText();
    Residencia residencia = (Residencia) fResidencia.getSelectedItem();

    Morador morador = new Morador(idMorador, nome, dataNascimento, rg, cpf, residencia.getId());

    try {
      this.moradorController.update(morador);
      JOptionPane.showMessageDialog(this, "Morador alterado com sucesso!");
      fNome.setText("");
      fNascimento.setText("");
      fRG.setText("");
      fCPF.setText("");
      fResidencia.setSelectedIndex(-1);
      close();
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Falha ao alterar morador, verifique o erro abaixo: \n" + ex.getMessage());
    }

  }//GEN-LAST:event_btnSalvarActionPerformed

