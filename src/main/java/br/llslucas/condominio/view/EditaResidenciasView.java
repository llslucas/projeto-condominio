  private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
    String rua = fRua.getText();
    Long numero = Long.parseLong(fNumero.getText());
    String cep = fCep.getText();
    String tipo = fTipo.getText();
    Morador proprietario = (Morador) fProprietario.getSelectedItem();
    Long proprietarioId = proprietario != null ? proprietario.getId() : null;

    Long condominioId = ((Condominio) fCondominio.getSelectedItem()).getId();

    Residencia condominio = new Residencia(residenciaId, rua, numero, cep, tipo, proprietarioId, condominioId);

    try {
      this.residenciaController.update(condominio);
      JOptionPane.showMessageDialog(this, "Residência alterada com sucesso!");
      fRua.setText("");
      fNumero.setText("");
      fCep.setText("");
      fTipo.setText("");
      fProprietario.setSelectedIndex(-1);
      fCondominio.setSelectedIndex(-1);
      close();
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Falha ao alterar residência, verifique o erro abaixo: \n" + ex.getMessage());
    }

  }//GEN-LAST:event_btnCadastrarActionPerformed

