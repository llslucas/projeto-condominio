  @Override
  public void setVisible(boolean aFlag) {
    super.setVisible(aFlag);

    if (aFlag) {
      if (residenciaId != null) {
	try {
	  Residencia residencia = residenciaController.getById(residenciaId);

	  fRua.setText(residencia.getRua());
	  fNumero.setText(residencia.getNumero().toString());
	  fCep.setText(residencia.getCep());
	  fTipo.setText(residencia.getTipo());

	  Morador proprietario;
	  if (residencia.getProprietarioId() != null) {
	    proprietario = moradorController.getById(residencia.getProprietarioId());
	  } else {
	    proprietario = null;
	  }

	  Condominio condominio = condominioController.getById(residencia.getCondominioId());

	  fProprietario.removeAllItems();
	  fProprietario.addItem(null);
	  this.moradorController.list().forEach((i, morador) -> {
	    fProprietario.addItem(morador);
	  });
	  fProprietario.setSelectedItem(proprietario);

	  fCondominio.removeAllItems();
	  this.condominioController.list().forEach((i, cond) -> {
	    fCondominio.addItem(cond);
	  });
	  fCondominio.setSelectedItem(condominio);

	} catch (Exception ex) {
	  JOptionPane.showMessageDialog(this, "Falha na conexão com o banco de dados", "Erro", JOptionPane.ERROR_MESSAGE);
	  super.setVisible(false);
	}
      } else {
	JOptionPane.showMessageDialog(this, "Nenhuma residência foi selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
      }
    } else {
      residenciaId = null;
      fRua.setText("");
      fNumero.setText("");
      fCep.setText("");
      fTipo.setText("");
      fProprietario.setSelectedIndex(-1);
      fCondominio.setSelectedIndex(-1);
    }
  }

  private void close() {
    JPanel parent = (JPanel) this.getParent();
    CardLayout layout = (CardLayout) parent.getLayout();
    layout.show(parent, "residencias");
  }

