  private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    int row = table.getSelectedRow();

    if (row == -1) {
      JOptionPane.showMessageDialog(this, "Selecione uma linha primeiro!", "Erro", JOptionPane.ERROR_MESSAGE);
      return;
    }

    Long id = Long.valueOf((String) table.getValueAt(row, 0));
    Condominio condominio;

    try {
      condominio = this.controller.getById(id);
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "O condomínio selecionado não existe.", "Erro", JOptionPane.ERROR_MESSAGE);
      return;
    }

    int opt = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o condominio?", "Aviso", JOptionPane.OK_CANCEL_OPTION);

    try {
      if (opt == 0) {
	this.controller.delete(condominio);
	preencherTabela();
      }
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Falha ao excluir o condominio, verifique o erro abaixo: \n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_btnExcluirActionPerformed

