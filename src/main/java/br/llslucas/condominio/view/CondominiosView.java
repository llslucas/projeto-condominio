  public void updateRow() {
    int row = table.getSelectedRow();

    if (row == -1) {
      return;
    }

    Long id = Long.parseLong((String) table.getValueAt(row, 0));
    String cnpj = (String) table.getValueAt(row, 1);
    String razaoSocial = (String) table.getValueAt(row, 2);

    Condominio condominio = new Condominio(id, cnpj, razaoSocial);

    try {
      this.controller.update(condominio);
      JOptionPane.showMessageDialog(this, "Condominio atualizado com sucesso!");
      preencherTabela();
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Falha ao excluir o condominio, verifique o erro abaixo: \n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
  }

  public final void preencherTabela() {
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    table.setModel(model);
    table.selectAll();
    model.setRowCount(0);

    Map<Long, Condominio> condominios;

    try {
      condominios = controller.list();

      condominios.forEach((i, condominio) -> {
	Object[] data = {String.valueOf(i), condominio.getCnpj(), condominio.getRazaoSocial()};
	model.addRow(data);
      });
    } catch (Exception ex) {
      JOptionPane.showMessageDialog(this, "Não foi possível realizar a conexão com o banco de dados, verifique se está ativo ou se foi configurado corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
  }

